package BalancedSymbols;


public class Symbol {

	private char symboltype;
	
	
	public Symbol() {
			
	}
	
	public Symbol(char symboltype) {
		this.symboltype = symboltype;
	}

	public char getSymboltype() {
		return symboltype;
	}

	public void setSymboltype(char symboltype) {
		this.symboltype = symboltype;
	}
	
	
	
}
